package stringMethodExamples;

public class JavaStringcompareTo {

	public static void main(String[] args) {
		
		    String str1 = "apple";
	        String str2 = "banana";
	        
	        int result = str1.compareTo(str2);
	        System.out.println("1st Example output:");
	        System.out.println(result);
	        

		    String one = "apple";
	        String two = "apple";
	        
	        int result1 = one.compareTo(two);
	        System.out.println("2nd Example output:");
	        System.out.println(result1);
	        
	        String third = "1234";
	        String fourth = "2345";
	        
	        int result3 = third.compareTo(fourth);
	        System.out.println("3rd Example output:");
	        System.out.println(result3);
	        
	        
	}

}
