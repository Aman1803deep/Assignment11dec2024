package stringMethodExamples;

public class JavaStringlength {

	public static void main(String[] args) {
		   String name = "Amandeep, Kaur!";
		   System.out.println("1st Example output:");
	       System.out.println("Length of the string: " + name.length());
	       
	       String empty = "";
	       System.out.println("2nd Example output:");
	       System.out.println("Length of the string: " + empty.length());
	       
	       String hello = "Hello\nWorld";
	       System.out.println("3rd Example output:");
	       System.out.println("Length of the string: " + hello.length());

	}

}
